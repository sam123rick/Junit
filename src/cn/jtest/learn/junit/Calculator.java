package cn.jtest.learn.junit;

public class Calculator {

    /**
     *  plus
     * @param a
     * @param b
     * @return
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     *  minus
     * @param a
     * @param b
     * @return
     */
    public int minus(int a, int b) {
        return a - b;
    }
}
